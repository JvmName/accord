    getWords(count) {
        const picked = [];
        const used = new Set();

        while (picked.length < count && picked.length < WORDS.length) {
            const idx = Math.floor(Math.random() * WORDS.length);
            if (!used.has(idx)) {
                used.add(idx);
                picked.push(WORDS[idx].word.replace(/\s+/, '_'));
            }
        }

        return picked;
    }
